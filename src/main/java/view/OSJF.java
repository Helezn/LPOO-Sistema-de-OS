    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNovoActionPerformed
        CadastroOSJD telaCadastro = new CadastroOSJD(this, true);
        telaCadastro.setVisible(true);
        OrdemServico novaOS = telaCadastro.getOS();

        if (!jpa.conexaoAberta()) {
            jpa = new PersistenciaJPA();
        }
        try {
            jpa.persist(novaOS);
        } catch (Exception ex) {
            System.err.println("ERRO AO PERSISTIR NOVA OS: " + ex);
        }
        loadOS();
    }//GEN-LAST:event_btnNovoActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        OrdemServico osSel = lstOS.getSelectedValue();
        if (osSel == null) {
            JOptionPane.showMessageDialog(null, "Selecione uma OS para remover");
        } else {
            int opDel = JOptionPane.showConfirmDialog(rootPane,
                    "Tem certeza que deseja remover a OS: " + osSel + "?");
            if (opDel == JOptionPane.YES_OPTION) {
                if (!jpa.conexaoAberta()) {
                    jpa = new PersistenciaJPA();
                }
                try {
                    jpa.remover(osSel);
                } catch (Exception ex) {
                    System.err.println("ERRO AO REMOVER OS: " + ex);
                }
                loadOS();
            }
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
         OrdemServico osSelecionada = lstOS.getSelectedValue();
    if (osSelecionada != null) {
        CadastroOSJD telaCadastro = new CadastroOSJD(this, true);
        telaCadastro.setOS(osSelecionada);
        telaCadastro.setVisible(true);
        loadOS();
    } else {
        JOptionPane.showMessageDialog(rootPane, "Selecione uma OS para editar!");
    }
    }//GEN-LAST:event_btnEditarActionPerformed

